@Composable
fun AppTheme(
    selectedThemeMode: ThemeMode = ThemeMode.SYSTEM,
    content: @Composable() () -> Unit
) {
    val systemInDarkTheme = isSystemInDarkTheme()

    val darkTheme = when (selectedThemeMode) {
        ThemeMode.LIGHT -> false
        ThemeMode.DARK -> true
        ThemeMode.SYSTEM -> systemInDarkTheme
    }

    val colorScheme = if (darkTheme) darkScheme else lightScheme
    val appColorsPalette = if (darkTheme) darkPalette else lightPalette

    ChangeStatusBarColor(
        darkIcons = !darkTheme
    )

    CompositionLocalProvider(
        LocalAppDimens provides CompactDimens,
        LocalAppColorsPalette provides appColorsPalette,
        AppLocale provides appLocale,
    ) {

        MaterialTheme(
            colorScheme = colorScheme,
            typography = AppTypography,
            content = content
        )
    }
}

val LocalAppColorsPalette = staticCompositionLocalOf { AppColorPalette() }
val MaterialTheme.dimens
    @Composable get() = LocalAppDimens.current

val LocalAppDimens = compositionLocalOf {
    CompactDimens
}


val MaterialTheme.appColors
    @Composable get() = LocalAppColorsPalette.current